#include "common.h"
#include "avr_compiler.h"

void POWER_SAVE_ENABLE()
{
	
	PR.PRGEN=0x5F;
	PR.PRPA=0x07;
	PR.PRPB=0x07;
	PR.PRPC=0x7F;
	PR.PRPD=0x7F;
	PR.PRPE=0x7F;
	PR.PRPF=0x7F;
}

void POWER_SAVE_DISABLE()
{
	PR.PRGEN=0x00;
	PR.PRPA=0x00;
	PR.PRPB=0x00;
	PR.PRPC=0x00;
	PR.PRPD=0x00;
	PR.PRPE=0x00;
	PR.PRPF=0x00;
}

void EXTERNAL_INT_PINDO()
{
	PORTD_PIN2CTRL = 0x00|PORT_OPC_TOTEM_gc|PORT_ISC_RISING_gc;
	//PORTD_DIRCLR=0x01;
	PORTD.INTCTRL=PORT_INT0LVL_MED_gc;
	PORTD.INT0MASK=0x04;
}